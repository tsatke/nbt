var Result interface{}

func BenchmarkDecode_Bigtest(b *testing.B) {
	var res Tag
	var err error

	b.ReportAllocs()
	b.ResetTimer()

	for i := 0; i < b.N; i++ {
		// This is actually faster if the timer is not stopped and started again just to exclude this.
		// Also, this is probably what every use has to do anyways, so it's not too bad to include that also.
		rd := bytes.NewReader(bigtestData[:])
		dec := NewDecoder(rd, binary.BigEndian)

		res, err = dec.ReadTag()
		if err != nil {
			panic(err)
		}
	}

	Result = res
}

